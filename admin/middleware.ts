import { NextResponse } from 'next/server'
import type { NextRequest } from 'next/server'

export function middleware(request: NextRequest) {
  const { pathname } = request.nextUrl

  // Public routes that don't require authentication
  const publicRoutes = ['/login-form']
  
  // Protected routes that require authentication
  const isProtectedRoute = !publicRoutes.includes(pathname) && pathname !== '/'

  // Note: Authentication check is handled client-side by ProtectedRoute component
  // This middleware just ensures proper routing structure
  // The actual auth check happens in components using localStorage

  return NextResponse.next()
}

export const config = {
  matcher: [
    /*
     * Match all request paths except for the ones starting with:
     * - api (API routes)
     * - _next/static (static files)
     * - _next/image (image optimization files)
     * - favicon.ico (favicon file)
     */
    '/((?!api|_next/static|_next/image|favicon.ico).*)',
  ],
}

